<?php
function sumarexample($a, $b) {
    return $a + $b;
}

function restarexample($a, $b) {
    return $a - $b;
}

function multiplicarexample($a, $b) {
    return $a * $b;
}

function dividirexample($a, $b) {
    return $b != 0 ? $a / $b : 'Error: División entre cero';
}

